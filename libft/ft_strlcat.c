#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t size)
{
	size_t		i;
	size_t		j;
	size_t		res;

	j = 0;
	i = 0;
	res = 0;
	while (dst[i])
		i++;
	while (src[res])
		res++;
	if (size < i)
		res += size;
	else
		res += i;
	while (src[j] && i + 1 < size)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (res);
}
